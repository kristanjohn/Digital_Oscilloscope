/*
 * @brief	Check which flag is set
 */
uint8_t lcd_check_flag(void) {
	uint16_t ai;
	uint16_t checkedIndex = 0;
	// i will map to PaketType
	for (ai = 0; ai < 0x35; ai++) {
		if (Flags[ai] == 1) {
			// To be safe clear all flags
			checkedIndex = ai;
		}
		Flags[ai] = 0;
	}
	return checkedIndex;
}

/*
 * @brief	Redraw the connect indicator when device is connected to
 * 				the PC software
 */
void lcd_connected(void) {
	connected = 1;
	drawConnected(connected);
}

