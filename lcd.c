/*
 * @brief	Controls the touch configuration for the main menu
 * @params	i: the y-value of the touch
 * 			j: the x-value of the touch
 * @return	the new current menu
 */
uint16_t touchMainMenu(uint16_t i, uint16_t j) {

	// Go to ranges menu
	if (i >= 90 && i <= 120 && j <= 300 && j >= 230) {
		clearMain();
		drawRangesMenu();
		return RANGES_MENU;
	} else if (i >= 90 && i <= 120 && j >= 130 && j <= 180) {
		// Go to the trigger menu
		clearMain();
		drawTriggerMenu();
		return TRIGGER_MENU;
	} else if (i >= 20 && i <= 50 && j >= 130 && j <= 180) {
		// Go to the function gen menu
		clearMain();
		drawFunctionMenu();
		return FUNCTION_MENU;
	} else if (i >= 20 && i <= 50 && j <= 300 && j >= 230) {
		// Go to the backlight menu
		clearMain();
		drawBacklightMenu();
		return BACKLIGHT_MENU;
	} else if (i >= 20 && i <= 50 && j >= 20 && j <= 80) {
		if (coupling_ac_dc) {
			// Change to AC coupling
			coupling_ac_dc = 0;
			Flags[TYPE_CHAN_COUP_AC] = 1;

			Make_Button(0, 0, 68, 100, 0x07E0);
			drawLetter(24, 30, 2, A);
			drawLetter(24, 50, 2, C);

			Make_Button(72, 0, 68, 100, 0xF800);
			drawLetter(96, 30, 2, D);
			drawLetter(96, 50, 2, C);
		}
	} else if (i >= 90 && i <= 120 && j >= 20 && j <= 80) {
		if (!coupling_ac_dc) {
			// Change to DC coupling
			coupling_ac_dc = 1;
			Make_Button(0, 0, 68, 100, 0xF800);
			drawLetter(24, 30, 2, A);
			drawLetter(24, 50, 2, C);

			Make_Button(72, 0, 68, 100, 0x07E0);
			drawLetter(96, 30, 2, D);
			drawLetter(96, 50, 2, C);
			Flags[TYPE_CHAN_COUP_DC] = 1;
		}
	} else if (i >= 210 && i <= 240 && j >= 290 && j <= 320) {
		// Turn off backlight
		GPIOPinWrite(LED_PORT, LED, 0);
		SysCtlDelay(1000000);
		return BACKLIGHT_OFF;
	}
	return MAIN_MENU;
}

