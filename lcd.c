/*
 * @brief	Write data to the touch screen
 * @params	data: data to be written
 */
void touchWriteData(unsigned char data)
{
	unsigned char temp;
	unsigned char nop;
	unsigned char count;

	temp=data;
	GPIOPinWrite(T_CLK_PORT, T_CLK, 0x0);

	for(count = 0; count < 8; count++)
	{
		if(temp & 0x80)
			GPIOPinWrite(T_DIN_PORT, T_DIN, T_DIN);
		else
			GPIOPinWrite(T_DIN_PORT, T_DIN, 0x0);
		temp = temp << 1;
		GPIOPinWrite(T_CLK_PORT, T_CLK, 0x0);
		nop++;
		GPIOPinWrite(T_CLK_PORT, T_CLK, T_CLK);
		nop++;
	}
}

/*
 * @brief	Read data from the touch screen
 * @return	the data read from the touch screen
 */
uint16_t touchReadData()
{
	unsigned char nop;
	uint16_t data = 0;
	unsigned char count;
	for(count = 0; count < 12; count++)
	{
		data <<= 1;
		GPIOPinWrite(T_CLK_PORT, T_CLK, T_CLK);
		nop++;
		GPIOPinWrite(T_CLK_PORT, T_CLK, 0x0);
		nop++;
		if (GPIOPinRead(T_DOUT_PORT, T_DOUT))
			data++;
	}
	return(data);
}

/*
 * @brief	Read touch
 */
void touchRead()
{
	unsigned long tx=0;
	unsigned long ty=0;
	uint16_t i;

	GPIOPinWrite(T_CS_PORT, T_CS, 0x0);

	for (i=0; i<PREC_TOUCH_CONST; i++)
	{
		touchWriteData(0x90);
		GPIOPinWrite(T_CLK_PORT, T_CLK, T_CLK);
		GPIOPinWrite(T_CLK_PORT, T_CLK, 0x0);
		ty+=touchReadData();

		touchWriteData(0xD0);
		GPIOPinWrite(T_CLK_PORT, T_CLK, T_CLK);
		GPIOPinWrite(T_CLK_PORT, T_CLK, 0x0);
		tx+=touchReadData();
	}

	GPIOPinWrite(T_CS_PORT, T_CS, T_CS);

	TP_X=tx/PREC_TOUCH_CONST;
	TP_Y=ty/PREC_TOUCH_CONST;
}

